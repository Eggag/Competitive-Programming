template<class T> T gcd(T a, T b){ return ((b == 0) ? a : gcd(b, a % b)); }

int n, x;

bool check(int g){
	if(g == (n - 1)) return 1;
	int r = (g + 1), l = n - (g + 1) + 1;
	if(l > r || l < 1 || r > n) return 0;
	ll tot = ((ll)(g) * (ll)(g + 1)) / 2;
	ll d = r - l + 1;
	if(d > 1) return 1;
	else{
		if((tot - abs(x - l)) & 1) return 0;
		return 1;
	}
}

int main(){
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	//freopen("input.in", "r", stdin);
	//freopen("output.out", "w", stdout);
	cin >> n >> x;
	int l = 0, r = n - 1;
	while(l < r){
		int mid = (l + r) / 2;
		if(check(mid)) r = mid;
		else l = mid + 1;
	}
	cout << l << '\n';
	return 0;
}
