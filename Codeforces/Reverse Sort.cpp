template<class T> T gcd(T a, T b){ return ((b == 0) ? a : gcd(b, a % b)); }

void solve(){
	int n;
	cin >> n;
	string s;
	cin >> s;
	int l = 0, r = n - 1;
	vi pos;
	while(l < r){
		while(l < n && s[l] != '1') l++;
		while(r >= 0 && s[r] != '0') r--;
		if(l < r) pos.pb(l), pos.pb(r), l++, r--;
	}
	sort(all(pos));
	if(!pos.size()) cout << 0 << '\n';
	else{
		cout << 1 << '\n';
		cout << (int)(pos.size()) << ' ';
		for(int x : pos) cout << x + 1 << ' ';
		cout << '\n';
	}
}

int main(){
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	//freopen("input.in", "r", stdin);
	//freopen("output.out", "w", stdout);
	int t;
	cin >> t;
	while(t--) solve();
	return 0;
}
