void solve(){
	int n, d;
	cin >> n >> d;
	if(d <= n){
		cout << "YES" << endl;
		return;
	}
	int nm = sqrt(d);
	for(int i = -1000; i <= 1000; i++){
		if((nm + i) > 0 && (nm + i) < n){
			int num = nm + i;
			int num1 = num + ((d + num) / (num + 1));
			if(num1 <= n){
				cout << "YES" << endl;
				return;
			}
		}
	}
	cout << "NO" << endl;
}

