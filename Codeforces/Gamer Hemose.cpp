void solve(){
	ll n, H;
	cin >> n >> H;
	vector<ll> a(n);
	ll mx = 0, mx1 = 0;
	rep(i, n){
		cin >> a[i];
		if(a[i] > mx){
			mx1 = mx;
			mx = a[i];
		}
		else if(a[i] > mx1) mx1 = a[i];
	}
	ll ans = 2 * (H / (mx + mx1));
	H %= (mx + mx1);
	if(H > 0) ans++;
	if(H > mx) ans++;
	cout << ans << '\n';
}

