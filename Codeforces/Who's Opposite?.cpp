template<class T> T gcd(T a, T b){ return ((b == 0) ? a : gcd(b, a % b)); }

void solve(){
	ll a, b, c;
	cin >> a >> b >> c;
	if(b < a) swap(a, b);
	ll n = 2 * (b - a);
	ll d = 0LL;
	if(c > n || a > n || b > n){
		cout << -1 << '\n';
		return;
	}
	if(c > a && c < b) d = b + (c - a);
	else if(c < a) d = b - (a - c);
	else d = a + (c - b);
	if(d > n) d -= n;
	if(d < 1) d += n;
	cout << d << '\n';
}

