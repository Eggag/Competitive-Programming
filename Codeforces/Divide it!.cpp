int gcd(int a, int b){ return ((b == 0) ? a : gcd(b, a % b)); }

void solve(){
	ll n;
	cin >> n;
	int ans = 0;
	while(n > 1){
		if(n % 2 == 0) n /= 2, ans++;
		else if(n % 3 == 0) n /= 3, ans += 2;
		else if(n % 5 == 0) n /= 5, ans += 3;
		else{
			ans = -1;
			break;
		}
	}
	cout << ans << endl;
}

