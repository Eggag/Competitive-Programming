void solve(){
	ll a, s;
	cin >> a >> s;
	ll sm = 2 * a;
	if(sm > s){
		cout << "No\n";
		return;
	}
	ll rem = s - sm;
	for(int i = 61; i >= 0; i--) if(!(a & (1LL << i))){
		if((1LL << i) <= rem) rem -= (1LL << i);
	}
	cout << (!rem ? "Yes" : "No") << '\n';
}

